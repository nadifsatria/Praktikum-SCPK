% --- Executes on button press in hasil.
function hasil_Callback(hObject, eventdata, handles)
% hObject    handle to hasil (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
global p
 set(handles.uitable2, 'Data' ,p.MyData)
a1 = str2double(get(handles.edit5,'string'));
a2 = str2double(get(handles.edit6,'string'));
a3 = str2double(get(handles.edit7,'string'));
a4 = str2double(get(handles.edit8,'string'));
a5 = str2double(get(handles.edit16,'string'));
w = [a1 a2 a3 a4 a5];

k1 = str2double(get(handles.edit9,'string'));
k2 = str2double(get(handles.edit10,'string'));
k3 = str2double(get(handles.edit11,'string'));
k4 = str2double(get(handles.edit12,'string'));
k5 = str2double(get(handles.edit17,'string'));
k = [k1 k2 k3 k4 k5];
[m n]=size (p.MyData); %matriks m x n dengan ukuran sebanyak variabel x (input)
R=zeros (m,n); %membuat matriks R, yang merupakan matriks kosong
Y=zeros (m,n); %membuat matriks Y, yang merupakan titik kosong
for j=1:n,
 if k(j)==1, %statement untuk kriteria dengan atribut keuntungan
  R(:,j)=p.MyData(:,j)./max(p.MyData(:,j));
 else
  R(:,j)=min(p.MyData(:,j))./p.MyData(:,j);
 end;
end;
for i=1:m,
 V(i)= sum(w.*R(i,:));
end; 

hasil= max(V);
set(handles.edit23,'string',(hasil));%menampilkan output dari hasil

set(handles.text24,'string',(V(1)));
set(handles.text25,'string',(V(2)));
set(handles.text26,'string',(V(3)));
set(handles.text27,'string',(V(4)));
set(handles.text28,'string',(V(5))); 

